function somarValores() {
    {
        formSoma.resSoma.value = parseFloat(formSoma.valor1.value) + parseFloat(formSoma.valor2.value)
    }
}

function subtrairValores() {
    {
        formSub.resSub.value = parseFloat(formSub.valor1.value) - parseFloat(formSub.valor2.value)
    }
}

function multiplicarValores() {
    {
        formMult.resMult.value = parseFloat(formMult.valor1.value) * parseFloat(formMult.valor2.value)
    }
}

function dividirValores() {
    {
        formDiv.resDiv.value = parseInt(formDiv.valor1.value) / parseInt(formDiv.valor2.value)
    }
}